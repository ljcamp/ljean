<p align="justify" id="content"> <a name="top"><h3>Publications</h3></a>
<br>

    My research core interest is human-centered security and privacy. Security and privacy are the primary sources of online risk.  My interest spans domains from small embedded home-based computing to SDN; from helping an elder avoid fraud to an activist avoid surveillance through malicious route manipulation. The core of my interest is the interaction of the human and the technical, empowering people by building security and privacy into the information substrate.
<br>

    I have categorized my work into somewhat overlapping
    categories. For example, some work on risk assessment took place
    in the context of security for in-home computing. Usable security
    grounded in risk communication for network operators similarly
  spans multiple category. 
<br>
    <br>
Most recently updated is the slide set from Real World Crypto <A href="http://ljean.com/files/RealWordCryptov4.pdf">http://ljean.com/files/RealWordCryptov4.pdf</a>
