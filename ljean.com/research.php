<br>Security as Risk<br>
<A href= "http://www.ljean.com/files/riskPosted.pdf"><b>Computer Security is Risk Communication</b></a><br>
<em>Want Technically Naive People to Adopt Security Technology? Talk to them in their own terms...<br></em>
Individual security solutions have not been adopted even when individuals have expressed their desire to do so. Our experiment suggests one contributing factor is that the rich array of metaphors used by computer security professionals fail to align with individual's mental models. Speaking of <q>phishing</q>, <q>worms</q>, and <q>infections</q> is not helpful risk communication.
