<br>Experimentation<br>
<em>Want Security? Build Privacy.<br></em>
There is some question as to why end users do not adopt security technologies. We argue that this is typical of users to behave as human beings in the domain of risk and uncertainty. That is, there are consistent biases that determine if risks are acceptable or not. Responses to risk are determined more by the perception of benevolence of the creator of the risk than by the magnitude of the risk.  Here is <A href=files/MTW06camp.pdf>Trust on the Web, a Tale of User Deceit</a>.
