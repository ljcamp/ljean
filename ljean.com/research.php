<em><A href=NetTrust/>Net Trust: Defeating Social Engineering</a></em><br>
Description:<em><A href="http://www.youtube.com/watch?v=IGBKI1QBAr8">The Tech Talk Overview</a></em> describes how Net Trust works. Current trust mechanisms are built for computers, not humans, despite the reams of available research on human trust decisions. In fact, the most common trust devices (e.g., seals, domain names) require the cooperation of the malicious to function. We have developed  a system to use social networks to inform trust decisions. Initial users tests show that Net Trust alters trust behaviors, providing information to people that makes them more trusting of some sites and less trusting of others.
