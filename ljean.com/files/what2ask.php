    <strong>Congrats! You Got Tenure. How to Write a Promotion Letter</strong>
<br><br>
    The first semester you are happliy tenured you may get the first request for a tenure letter. Alas, you have not seen many of these. Pay attention to content and form when reading other tenure letters as you participate in reviews.
    <ol>
        <li>  State your recommendation up front.</li>
        <li>  Introduce yourself, and identify area overlap and differences in terms of reasearch areas.</li>
        <li>  Put in a general paragraph about interaction with candidate, identify any Conflict of Interest.</li>
        <li>  You get three papers. For each paper, one paragraph includes a two sentence description of the work, a sentence or so about how it fits in the body of knowledge. What is unique? Define its contributions.  </li>
        <li>  Repeat for paper 2 in one paragraph.</li>
        <li>  Repeat for paper 3 in one paragraph.</li>
        <li>  Unless you have particular insight, something polite about why do we ask people from outside the institution to talk about this person's teaching? This remains a mystery to me.</li>
        <li> What is the person's overall impact? You may include your thoughts about impact trajectory particularly if you are requested to do so in the letter from the Dean or Chair. Venues of publication, participation in the community, and funding may be discussed  here as appropriate for research area.</li>
        <li> Repeat recommendation in closing.</li>
    </ol>
