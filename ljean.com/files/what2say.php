        <strong>Congrats! You Got Tenure. How to Write a Promotion Letter:</strong>
<br><br>
    The first semester you are happliy tenured you may get the first request for a tenure letter. Alas, you have not seen many of these. Pay attention to content and form when reading other tenure letters as you participate in reviews.
    <ol>
        <li>  State your recommendation up front.</li>
        <li>  Introduce yourself, and identify area overlap and differences in terms of reasearch areas.</li>
        <li>  Put in a general paragraph about interaction with candidate, identify any Conflict of Interest.</li>
    <li>  You get three papers, a cv, and a research statement.
   Read the research statement first. The research statement places the papers in a research trajectory. For example,maybe someone never followed up on apromising work because they went in another direction. Or perhaps they lost resources and had to re-invent themselves. Or maybe all three are just different bits of the elephant, with the whole animal sketched in the research statement.
<li> Then for each paper write a few sentences describing of the work, a sentence or so about how it fits in the body of knowledge, e.g., this is a study of argle bargle in the tradition of Zzzzz. Then provide some detail. Then answer questions about the quality and placement in the domain.  What is unique? Define its contributions.  </li>
        <li>  Repeat for paper 2.</li>
        <li>  Repeat for paper 3.</li>
	At the end of this dicussion of papers, there should be two or three pages. 
        <li>  Teaching: unless you have particular insight, something polite.  I usually mention quality of presentation and explantion at conferences. Why do we ask people from outside the institution to talk about this person's teaching? This remains a mystery to me.</li>
        <li> What is the person's overall impact? You may include your thoughts about impact trajectory particularly if you are requested to do so in the letter from the Dean or Chair. Venues of publication, participation in the community, and funding may be discussed  here as appropriate for research area.</li>
        <li> Repeat recommendation in closing.</li>
    </ol>
        
