        <strong>Congrats! Your student graduated. How to Write a Recomendation Letter:</strong>
        <br><br>
        I hope to get feedback for this as I am not the best. So the only way to obtain feedback is to ask, so let me know. For a doctoral student, read the student's research and teaching statement again. If this is your student you should have read these already; however, they are customized for different positions.  For others, look at the student's resume. Your recommendation should align with the student's self-presentation.
        <ol>
            <li>  State your recommendation up front.</li>
             <li>  Customize your recommendation and explain fit for the position.</li>
            <li>  Introduce yourself, and identify how long and in what roles you have worked with the student (e.g., recruited, student, RA, TA, etc).</li>
            <li>  Put in a general paragraph about the student's dissertation, thesis, or performance in your course. For doctoral students, what was the role of the student in generating the dissertation? This also illustrates how the student will create her own research path. This should reflect or echo the student's research statement. Otherwise there is a serious intellectual disconnect.</li>
            <li> For doctoral recommendations, either the dissertation as a whole or pick a few papers. For each paper, write something similar to but more brief than the tenure description. There is far more focus on the role of the student and less on impact. It is a bit soon for impact. 
            <li> Unless the position is in a teaching school, teaching is not the focus of the recommendation.  However, teaching is important to include if the student has teaching experience. If the student is a TA and that is not mentioned, some people will read that as a red flag.  </li>
            <li> Mentoring is important in every job. Professors will mentor students; employees will mentor future hire. Even in undergraduate group projects, there may be examples of mentoring. </li>
            <li> Repeat recommendation in closing.</li>
        </ol>

