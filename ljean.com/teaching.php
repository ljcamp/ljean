    <a href="http://www.ljean.com/classes/03_04/308/STP_308_03.html"><br>
    STP 308: Technical Design and Human Values</a><br>
    How are value and political choices designed into technology? How do distinct 
    values emerge from the same technologies? How can policy makers recognize 
    values and prevent hard-wired technological value choices? This class begins 
    with two weeks of theory, but the focus of this class is a series of information 
    technology case studies. 2001-2002 was the first year this course was offered, 
    under the name Design for Values.<br>
    <br>
    <a href="http://www.ljean.com/classes/03_04/301/STP_301_03.html">STP 301: 
    Security and Privacy</a><br>
    Technology is being presented in popular discourse as a savior from the threats 
    of terrorism, and conversely as threat to democracy with the hard-wiring of 
    big brother. Accountability and anonymity seem to be inherently opposed. The 
    course identifies necessary trade-offs, and illustrates that many of the current 
    security and privacy exchanges risk reducing both. Wireless identifiers on 
    every article of clothing and the Federal Total Information Awareness program 
    are topics in this course as they will be integral parts of our lives in a 
    few years. Framing the class discussion are the basics of choosing security 
    technologies for federal systems.<br>
    <br>
    CANCELED FOR 2003-2004<br>
    <br>
    <a href="http://www.ljean.com/classes/03_04/304/STP_304_03.html">STP 304: 
    Telecommunications Modeling Commerce</a><br>
    Who bought the first fax machine? How did e-mail arise? This course will focus 
    on systems with feedback. The course will begin with an explanation of feedback, 
    and then a series of lectures will focus on specific mechanisms for feedback 
    in the economic and technical realm. Readings and cases will provide real 
    world examples. Dynamic cost-based modeling with technical uncertainty. Technical 
    change presents an interesting set of problems for quantitative modeling. 
    This course was originally offered as &quot;Information and Telecommunications 
    Protocols: Modeling and Policy Analysis&quot; in 2002. Analysis of technology 
    policy problems in developing countries is a rich source of material for this 
    project-based course.

