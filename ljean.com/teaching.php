    <a href="http://www.ljean.com/classes/05_06/525/I590_06.html">Economics of Information 
    Security</a><br>
    The language of computer security suggest the range of analogies. A virus 
    is a medical problem, while an invasive worm brings to mind the problems of 
    ecosystems. Computer crime and intrusion detection argue that the problem 
    is one of criminal behavior. Firewalls suggest that the network itself is 
    a hostile force, that must be segregated into the conflagration beyond and 
    the safety within. The construction of demilitarized zones (DMZs) between 
    trusty local area networks and the wider network beyond argues that it is 
    war, not flames, on the network. Yet for all that is involved in computer 
    security, and all that is lost, there is a potential measure: dollars. 

